window.addEventListener('DOMContentLoaded', (event) => {
  var carousel = document.querySelector('#carousel_ae8f');
  var carouselInner = carousel.querySelector('.u-carousel-inner');
  var scrolling = false;
  var slides = carouselInner.getElementsByClassName('u-carousel-item');
  var slideIndex = 0;

  var options = {
    root: null,
    rootMargin: '0px',
    threshold: 1.0
  }

  var callback = function(entries, observer) {
    entries.forEach(entry => {
      scrolling = entry.isIntersecting;
    });
  };

  var observer = new IntersectionObserver(callback, options);
  observer.observe(carousel);

  window.addEventListener('wheel', function(e) {
  if (scrolling) {
    var lastSlideIndex = slides.length - 1;

    // Only increment or decrement slideIndex if not at the boundaries
    if (e.deltaY < 0 && slideIndex > 0) {
      // Scrolling up
      slideIndex--;
      // Prevent default scroll behavior
      e.preventDefault();
    } else if (e.deltaY > 0 && slideIndex < lastSlideIndex) {
      // Scrolling down
      slideIndex++;
      // Prevent default scroll behavior
      e.preventDefault();
    }

    // Hide all slides
    for (var i = 0; i < slides.length; i++) {
      slides[i].classList.add("permanent-fade-in");
      slides[i].classList.remove('u-active');
      
      console.log(slides[i]);
    }

    // Show the current slide
    slides[slideIndex].classList.add('u-active');
  }
}, { passive: false });
});
